                                      ),
                                      DropdownMenuItem(
                                        value: 'dosen',
                                        child: Text('Dosen'),
                                      ),
                                    ],
                                    onChanged: (value) {
                                      setState(() {
                                        _selectedRole = value!;
                                      });
                                    },
                                  ),
                                  const SizedBox(height: 30),

                                  // Login Button
                                  ElevatedButton(
                                    onPressed: _isLoading ? null : _login,
