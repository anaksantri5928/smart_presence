                  const SizedBox(height: 80),

                  // Logo and Title
                  AnimatedBuilder(
                    animation: _fadeAnimation,
                    builder: (context, child) {
                      return Opacity(
                        opacity: _fadeAnimation.value,
                        child: Column(
                          children: [
                            Container(
                              width: 120,
                              height: 120,
